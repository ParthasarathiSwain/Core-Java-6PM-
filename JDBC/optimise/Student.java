package optimise;

public class Student {
	private int stdId;
	private String name;
	private String add;
	private String email;
	private String pass;
	
	public int getStdId() {
		return stdId;
	}
	public void setStdId(int stdId) {
		this.stdId = stdId;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getAdd() {
		return add;
	}
	public void setAdd(String add) {
		this.add = add;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getPass() {
		return pass;
	}
	public void setPass(String pass) {
		this.pass = pass;
	}
	
	@Override
	public String toString() {
		return "Student [stdId=" + stdId + ", name=" + name + ", add=" + add + ", email=" + email + ", pass=" + pass
				+ "]";
	}
	
	
	
}
